def get_messages(locale):
    locale = locale if locale in TRANSLATIONS else "en"
    return TRANSLATIONS[locale]


def translate(locale, key, default=None, **kwargs):
    locale = locale if locale in TRANSLATIONS else "en"
    message = TRANSLATIONS[locale].get(key)
    if message is None:
        message = TRANSLATIONS["en"].get(key, default if default is not None else key)
    if kwargs:
        try:
            return message.format(**kwargs)
        except Exception:
            return message
    return message
